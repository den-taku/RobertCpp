#include <iostream>
using namespace std;

int main()
{
    int num1 = 10;
    int num2 = 5;

    cout <<"num1とnum2に色々な演算を行います。" << endl;
    cout <<"num1+num2は" << num1+num2 <<"です。" <<endl;
    cout <<"num1-num2は" << num1-num2 <<"です。" <<endl;
    cout <<"num1*num2は" << num1*num2 <<"です。" <<endl;
    cout <<"num1/num2は" << num1/num2 <<"です。" <<endl;
    cout <<"num1%num2は" << num1%num2 <<"です。" <<endl;

    return 0;
}
